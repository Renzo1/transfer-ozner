const provider = new ethers.providers.AlchemyProvider(
  'goerli',
  process.env.API_KEY
);

const wallet = new ethers.Wallet(process.env.PRIVATE_KEY, provider);

async function main() {
  // const erc20 = await hre.ethers.getContractAt(tokenName, erc20Addr);
  const Contract = new ethers.Contract(bucketAddr, contractABI, wallet);
  const Token = new ethers.Contract(erc20Addr, tokenABI, wallet);

  await Token.approve(bucketAddr, 10);

  const tx = await Contract.drop(erc20Addr, 10);
  console.log(tx.hash);

  // Was the tx successful? Check the transaction receipt!
  // if it was, it will be in both the logs and events array. 
  // In this case we should emit Winner()
  const receipt = await tx.wait();
  console.log(receipt);
}

// We recommend this pattern to be able to use async/await everywhere
// and properly handle errors.
main()
  .then(() => process.exit(0))
  .catch(error => {
    console.error(error);
    process.exit(1);
  });
